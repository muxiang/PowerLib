namespace PowerLib.Winform.Controls
{
    /// <summary>
    /// 指定为此 XButton 显示何种对象（图像还是文本）
    /// </summary>
    public enum XButtonDisplayStyle
    {
        /// <summary>
        /// 既不显示图像，也不显示文本
        /// </summary>
        None,
        /// <summary>
        /// 只显示文本
        /// </summary>
        Text,
        /// <summary>
        /// 只显示图像
        /// </summary>
        Image,
        /// <summary>
        /// 同时显示图像和文本
        /// </summary>
        ImageAndText
    }
}
