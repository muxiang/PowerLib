using System;
using System.Collections.Specialized;
using System.Linq;
using System.Windows;
using System.Windows.Controls;

namespace PowerLib.WPF.Tools;

public static class DataGridAttach
{
    #region ApplyDefaultStyle

    public static readonly DependencyProperty ApplyDefaultStyleProperty =
        DependencyProperty.RegisterAttached(
            "ApplyDefaultStyle",
            typeof(bool),
            typeof(DataGridAttach),
            new PropertyMetadata(false, OnApplyDefaultStyleChanged));

    public static void SetApplyDefaultStyle(DependencyObject element, bool value)
        => element.SetValue(ApplyDefaultStyleProperty, value);

    public static bool GetApplyDefaultStyle(DependencyObject element)
        => (bool)element.GetValue(ApplyDefaultStyleProperty);

    private static readonly DependencyProperty IsHookedProperty =
        DependencyProperty.RegisterAttached(
            "IsHooked",
            typeof(bool),
            typeof(DataGridAttach),
            new PropertyMetadata(false));

    private static bool GetIsHooked(DependencyObject element)
        => (bool)element.GetValue(IsHookedProperty);

    private static void SetIsHooked(DependencyObject element, bool value)
        => element.SetValue(IsHookedProperty, value);

    private static void OnApplyDefaultStyleChanged(DependencyObject d, DependencyPropertyChangedEventArgs e)
    {
        DataGrid grid = d as DataGrid;
        if (grid == null)
            return;

        bool enable = e.NewValue is bool && (bool)e.NewValue;

        if (enable)
        {
            if (!GetIsHooked(grid))
            {
                grid.Columns.CollectionChanged += OnColumnsCollectionChanged;
                grid.AutoGeneratedColumns += OnAutoGeneratedColumns;
                grid.Loaded += OnGridLoaded;
                SetIsHooked(grid, true);
            }

            UpdateAllColumnStyles(grid);
        }
        else
        {
            if (GetIsHooked(grid))
            {
                grid.Columns.CollectionChanged -= OnColumnsCollectionChanged;
                grid.AutoGeneratedColumns -= OnAutoGeneratedColumns;
                grid.Loaded -= OnGridLoaded;
                SetIsHooked(grid, false);
            }
        }
    }

    private static void OnGridLoaded(object sender, RoutedEventArgs e)
    {
        DataGrid grid = sender as DataGrid;
        if (grid == null || !GetApplyDefaultStyle(grid))
            return;

        UpdateAllColumnStyles(grid);
    }

    private static void OnAutoGeneratedColumns(object sender, EventArgs e)
    {
        DataGrid grid = sender as DataGrid;
        if (grid == null || !GetApplyDefaultStyle(grid))
            return;

        UpdateAllColumnStyles(grid);
    }

    private static void OnColumnsCollectionChanged(object sender, NotifyCollectionChangedEventArgs e)
    {
        DataGrid grid = sender as DataGrid;
        if (grid == null || !GetApplyDefaultStyle(grid))
            return;

        if (e.Action == NotifyCollectionChangedAction.Remove || e.Action == NotifyCollectionChangedAction.Move)
            return;

        UpdateAllColumnStyles(grid);
    }

    #endregion

    #region OverrideExistingColumnStyle

    public static readonly DependencyProperty OverrideExistingColumnStyleProperty =
        DependencyProperty.RegisterAttached(
            "OverrideExistingColumnStyle",
            typeof(bool),
            typeof(DataGridAttach),
            new PropertyMetadata(false, OnAnyStyleChanged));

    public static void SetOverrideExistingColumnStyle(DependencyObject element, bool value)
        => element.SetValue(OverrideExistingColumnStyleProperty, value);

    public static bool GetOverrideExistingColumnStyle(DependencyObject element)
        => (bool)element.GetValue(OverrideExistingColumnStyleProperty);

    #endregion

    #region TextColumnStyle

    public static readonly DependencyProperty TextColumnStyleProperty =
        DependencyProperty.RegisterAttached(
            "TextColumnStyle",
            typeof(Style),
            typeof(DataGridAttach),
            new PropertyMetadata(null, OnAnyStyleChanged));

    public static void SetTextColumnStyle(DependencyObject element, Style value)
        => element.SetValue(TextColumnStyleProperty, value);

    public static Style GetTextColumnStyle(DependencyObject element)
