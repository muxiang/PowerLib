    public static Style GetCheckBoxColumnStyle(DependencyObject element)
        => (Style)element.GetValue(CheckBoxColumnStyleProperty);

    public static readonly DependencyProperty EditingCheckBoxColumnStyleProperty =
        DependencyProperty.RegisterAttached(
            "EditingCheckBoxColumnStyle",
            typeof(Style),
            typeof(DataGridAttach),
            new PropertyMetadata(null, OnAnyStyleChanged));

    public static void SetEditingCheckBoxColumnStyle(DependencyObject element, Style value)
        => element.SetValue(EditingCheckBoxColumnStyleProperty, value);

    public static Style GetEditingCheckBoxColumnStyle(DependencyObject element)
        => (Style)element.GetValue(EditingCheckBoxColumnStyleProperty);

    #endregion

    private static void OnAnyStyleChanged(DependencyObject d, DependencyPropertyChangedEventArgs e)
    {
        if (d is not DataGrid grid || !GetApplyDefaultStyle(grid))
            return;

        UpdateAllColumnStyles(grid);
    }

    private static void UpdateAllColumnStyles(DataGrid grid)
    {
        UpdateTextColumnStyles(grid);
        UpdateComboBoxColumnStyles(grid);
        UpdateCheckBoxColumnStyles(grid);
    }

    private static void UpdateTextColumnStyles(DataGrid grid)
    {
        Style elementStyle = GetTextColumnStyle(grid);
        Style editingStyle = GetEditingTextColumnStyle(grid);
        bool overwrite = GetOverrideExistingColumnStyle(grid);

        if (elementStyle != null)
        {
            foreach (DataGridTextColumn col in grid.Columns.OfType<DataGridTextColumn>())
            {
                if (!overwrite && col.ElementStyle != null && !LooksLikeDefaultColumnStyle(col.ElementStyle))
                    continue;

                col.ElementStyle = elementStyle;
            }
        }

        if (editingStyle != null)
        {
            foreach (DataGridTextColumn col in grid.Columns.OfType<DataGridTextColumn>())
            {
                if (!overwrite && col.EditingElementStyle != null && !LooksLikeDefaultColumnStyle(col.EditingElementStyle))
                    continue;

                col.EditingElementStyle = editingStyle;
            }
        }
    }

    private static void UpdateComboBoxColumnStyles(DataGrid grid)
    {
        Style elementStyle = GetComboBoxColumnStyle(grid);
        Style editingStyle = GetEditingComboBoxColumnStyle(grid);
        bool overwrite = GetOverrideExistingColumnStyle(grid);

        if (elementStyle != null)
        {
            foreach (DataGridComboBoxColumn col in grid.Columns.OfType<DataGridComboBoxColumn>())
            {
                if (!overwrite && col.ElementStyle != null && !LooksLikeDefaultColumnStyle(col.ElementStyle))
                    continue;

                col.ElementStyle = elementStyle;
            }
        }

        if (editingStyle != null)
        {
            foreach (DataGridComboBoxColumn col in grid.Columns.OfType<DataGridComboBoxColumn>())
            {
                if (!overwrite && col.EditingElementStyle != null && !LooksLikeDefaultColumnStyle(col.EditingElementStyle))
                    continue;

                col.EditingElementStyle = editingStyle;
            }
        }
    }

    private static void UpdateCheckBoxColumnStyles(DataGrid grid)
    {
        Style elementStyle = GetCheckBoxColumnStyle(grid);
        Style editingStyle = GetEditingCheckBoxColumnStyle(grid);
        bool overwrite = GetOverrideExistingColumnStyle(grid);

        if (elementStyle != null)
        {
            foreach (DataGridCheckBoxColumn col in grid.Columns.OfType<DataGridCheckBoxColumn>())
            {
                if (!overwrite && col.ElementStyle != null && !LooksLikeDefaultColumnStyle(col.ElementStyle))
                    continue;

                col.ElementStyle = elementStyle;
            }
        }

        if (editingStyle != null)
        {
            foreach (DataGridCheckBoxColumn col in grid.Columns.OfType<DataGridCheckBoxColumn>())
            {
                if (!overwrite && col.EditingElementStyle != null && !LooksLikeDefaultColumnStyle(col.EditingElementStyle))
                    continue;

                col.EditingElementStyle = editingStyle;
            }
        }
    }

    private static bool LooksLikeDefaultColumnStyle(Style style)
    {
        if (style == null)
            return true;

        // ľ­ŃéĹĐ¶¨ŁşÄ¬ČĎÉúłÉµÄ Column ElementStyle ÍůÍůĘÇ BasedOn=null ÇŇÖ»ÓĐÉŮÁż Setter
        // Äăµ÷ĘÔż´µ˝µÄˇ°Ö»ÓĐ4¸öSetterˇ±»ů±ľľÍĘÇŐâÖÖÇéżö
        if (style.BasedOn == null && style.Setters.Count > 0 && style.Setters.Count <= 4)
            return true;

        return false;
    }
}